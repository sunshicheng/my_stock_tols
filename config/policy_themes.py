def get_policy_themes_summary() -> str:
    """返回给 AI 的规划重点摘要（含具体表述）。"""
    lines = ["【国家十四五/十五五规划重点方向概要】（供结合当日要闻判断相关板块与政策依据）"]
    for t in POLICY_THEMES:
        lines.append(f"\n■ {t['theme']}")
        for item in t.get("outline", [])[:3]:
            lines.append(f"  · {item}")
        lines.append(f"  对应 A 股板块/概念：{', '.join(t['sectors'][:6])}")
    return "\n".join(lines)


def get_policy_themes_for_report() -> list[dict]:
    """返回用于报告展示的简化列表（主题 + 要点摘要）。"""
    return [
        {
            "theme": t["theme"],
            "summary": " ".join(t.get("outline", [])[:2]),
            "sectors": t["sectors"][:5],
        }
        for t in POLICY_THEMES
    ]
